def greet(name):
    """
    Returns greeting to given name
    :param str name: name to greet
    """
    return f'Hi {name}!'


def repeat(text, n):
    """
    Returns text repeated n times
    :param str text: text to repeat
    :param int n: how many times to repeat
    """
    return text * n


def divide(n):
    """
    Returns n divided by 2
    :param n: number to divide, integer or float
    """
    return n / 2


def increase(a):
    """
    Returns a increased by 10
    :param int a: base, integer or float
    """
    return a + 10
